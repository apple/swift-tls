/// This test case uses the test vectors from https://www.rfc-editor.org/rfc/rfc8448.html to
/// validate that our implementation of our key schedule-related functions matches the expected behaviour.
///
/// This does not validate the underlying HKDF implementation because that's owned by CryptoKit.
///
/// These tests use ECDHE keys that are not necessarily supported in the underlying implementation. This doesn't matter!
@available(SwiftTLS 0.1.0, *)
final class KeyScheduleTests: XCTestCase {
    func test1RTTExample() throws {
        /// This will run through an example flow of a 1RTT setup using RFC 8448.
        /// Note that this doesn't bother validating that secrets are `nil` before they're ready. That's because,
        /// if we calculated them right, they couldn't possibly be non-nil before they were ready!
        var scheduler = ClientSessionKeyManager<SHA256>()

        // First we get client hello. We feed it in, and confirm that our re-serialized version matches the input.
        var clientHello = Self.oneRTTClientHello
        let clientHelloBytes = try scheduler.sendingClientHello(clientHello: &clientHello, sessionToResume: nil, epsks: [], useRawEPSKs: false, currentTime: nil)
        XCTAssertEqual(clientHelloBytes, Self.oneRTTClientHelloBytes)

        // Then the server hello, feed it in. For now we don't check the early secrets.
        let ecdheSecret = try SymmetricKey(data: Self.oneRTTClientX25519Key.sharedSecretFromKeyAgreement(with: Self.oneRTTServerX25519Key.publicKey))
        XCTAssertNoThrow(try scheduler.postServerHello(ecdheSecret: ecdheSecret, serverHelloBytes: Self.oneRTTServerHello))

        // At this time we can validate the client and server handshake traffic secrets.
        XCTAssertEqual(scheduler.clientHandshakeTrafficSecret, Self.oneRTTClientHandshakeTrafficSecret)
        XCTAssertEqual(scheduler.serverHandshakeTrafficSecret, Self.oneRTTServerHandshakeTrafficSecret)

        // Now the server sends back three messages: EncryptedExtensions, Certificate, and CertificateVerify. We feed the first two.
        XCTAssertNoThrow(try scheduler.addPreFinishedMessageToTransportHash(Self.oneRTTServerEncryptedExtensions))
        XCTAssertNoThrow(try scheduler.addPreFinishedMessageToTransportHash(Self.oneRTTServerCertificateMessage))

        // Now at this stage we should be able to produce the certificate verify message from the server. We actually _can't_ because
        // the example uses RSA, which we don't have access to, but I've validated out-of-band that this is the correct set of bytes.
        XCTAssertEqual(try scheduler.dataToSignInServerCertificateVerify(), Self.oneRTTServerCertificateVerifySignedData)
        XCTAssertNoThrow(try scheduler.addPreFinishedMessageToTransportHash(Self.oneRTTServerCertificateVerify))

        // We should now be able to produce the server finished payload.
        XCTAssertEqual(ByteBuffer(bytes: try scheduler.serverFinishedPayload()), Self.oneRTTServerFinishedPayload)

        // Now send it.
        XCTAssertNoThrow(try scheduler.postServerFinished(serverFinishedBytes: Self.oneRTTServerFinishedMessage))

        // At this time we know the various traffic secrets, as well as the export secret.
        XCTAssertEqual(scheduler.clientApplicationTrafficSecret, Self.oneRTTClientAppTrafficSecret)
        XCTAssertEqual(scheduler.serverApplicationTrafficSecret, Self.oneRTTServerAppTrafficSecret)
        XCTAssertEqual(scheduler.exporterMasterSecret, Self.oneRTTExportMasterSecret)

        // We also know what the client finished will be.
        XCTAssertEqual(ByteBuffer(bytes: try scheduler.clientFinishedPayload()), Self.oneRTTClientFinishedPayload)

        // Now deliver the client finished.
        XCTAssertNoThrow(try scheduler.postClientFinished(clientFinishedBytes: Self.oneRTTClientFinishedMessage))

        // And finally we have the resumption master secret.
        XCTAssertEqual(scheduler.resumptionMasterSecret, Self.oneRTTResumptionMasterSecret)

        // We can validate that we derive the right PSK for a session ticket as well.
        XCTAssertEqual(
            try scheduler.generateSessionTicketPSK(ticketNonce: Self.oneRTTNewSessionTicketNonce),
            Self.oneRTTResumptionPreSharedKey
        )
    }
}

